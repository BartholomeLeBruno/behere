package com.esgi.behere.actor;

public class BeerType {

    private String name;

    public BeerType(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId() { return id; }

    public void setId(int id) { this.id = id; }

    private int id;


}
